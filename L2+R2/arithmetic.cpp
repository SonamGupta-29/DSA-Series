//  Enter two numbers and perform all arithmetic operations

#include <iostream>
using namespace std;

int main(){
    int a,b,c;
    cout<<"enter two number";
    cin>>a>>b;

// addition
    c=a+b;
    cout<<c<<endl;

    // substraction
    c=a-b;
    cout<<c<<endl;

    // multiplication
    c=a*b;
    cout<<c<<endl;

    // division
    c=a/b;
    cout<<c<<endl;

     // modulus
    c=a%b;
    cout<<c<<endl;

    return 0;
}
